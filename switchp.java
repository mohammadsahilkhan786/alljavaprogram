import java.util.*;
public class switchp {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
           int n=sc.nextInt();
           switch(n){
            case 1:
            System.out.println("hello");
            break;
            case 2:
            System.out.println("hayee");
            break;
            case 3:
            System.out.println("hiiii");
            break;
            default:
            System.out.println("invalid input");}
            sc.close();
           
        
        
    }

        
    
}
